public class dfasdfsaf {

	public static void main(String[] args) {
		
		for (int  i= 6;  i>0 ; --i) {
			System.out.print("");
			for (int j = 5; j > i; j--) {
				System.out.print("  ");
				for (int k = 0; k < 5 ; k++) {
					System.out.print("*");
				}
			}
			System.out.println(" ");
		}
	}
}
