// main 메소드를 포함하고 있는 클래스를 main 클래스라고 한다.
// main 클래스는 가장 먼저 실행될 클래스이다.
// 코드 자동완성 단축기 ctrl+spacebar
// 코드 실행 단축기 ctrl+f11
public class ex01_변수 {

	public static void main(String[] args) {
     //원하는 결과값을 콘솔창에 출력하는 문법 syso 적고 코드 자동완성 단축기 실행
          System.out.println("오 두개 배웠다");
		
          //변수의 선언 = int num
          int num = 10;
          //데이터 초기화 = 10;
          System.out.println(num*2);
          
          num = 15;
          System.out.println(num+1);
          
         //논리타입
          boolean b = true;
          b = true;
          System.out.println(b);
          
          //문자타입
          char c = 'c';
          System.out.println(c);
          
      
          
          
          //정수타입
          byte e = 123;
          System.out.println(e);
          
          short by = 15;
        		  System.out.println(by);
        //실수타입
        		float f = 10.3f;
        		System.out.println(f);
        		double v = 15.2;
        		System.out.println(v);
        		
        		
       // char는 정수타입중 하나이다.
        		char c6 = 'a';
        		int i6 = c6;
        		System.out.println(i6);
        		char c2 = 65;
        		System.out.println(c2);

        		
        		
        				
        		
        		
        		
        		 
        		  
	}

}
