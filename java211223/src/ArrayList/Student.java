package ArrayList;

public class Student {
	
	// 1. 필드
	private String name;
	private int nember;
	private int age;
	public Student(String name, int nember, int age) {
		super();
		this.name = name;
		this.nember = nember;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNember() {
		return nember;
	}
	public void setNember(int nember) {
		this.nember = nember;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	// 메소드
	
	
		
	

}
