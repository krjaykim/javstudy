
public class ex01_for {

	public static void main(String[] args) {
		
		//	for문 작성하기
		//	1. 초기화 구문
		//	2. 검사 조건 (초기화 구문으로 부터 언제까지 돌아가는지 확인 하는 조건)
		//	3. 반복 후 작업
		
		//	 1 ~ 3까지 반복하는 숫자 출력하기
		int i= 1;
		for( i = 1; i<4; i++)
			System.out.println("반복문 안의 i값"+i);
	System.out.println("반복문을 벗어난 i값 "+i );	
	}

}
